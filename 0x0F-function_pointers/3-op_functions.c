/**
 * op_add - add two integers
 * @a: int to add
 * @b: int to add
 *
 * Return: int sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract b from a
 * @a: int
 * @b: int to subtract
 *
 * Return: int difference a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first int
 * @b: second int
 *
 * Return: int product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - find quotient of two ints
 * @a: int numerator
 * @b: int denominator
 *
 * Return: int a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod to integers
 * @a: int numerator
 * @b: int denominator
 *
 * Return: int remainder of a/b (a%b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
